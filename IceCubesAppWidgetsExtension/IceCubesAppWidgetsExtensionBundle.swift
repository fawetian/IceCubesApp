// 文件功能说明：
// 该文件定义了 Ice Cubes 应用的小组件扩展包，作为小组件扩展的主入口点，注册和管理所有可用的小组件类型，包括最新帖子、标签帖子、列表帖子、提及通知和账户信息小组件。
// 技术点：
// 1. @main 属性 —— 标记为小组件扩展的主入口点。
// 2. WidgetBundle 协议 —— 组织和管理多个小组件的容器。
// 3. WidgetKit 框架 —— 苹果的小组件开发框架。
// 4. SwiftUI 集成 —— 使用 SwiftUI 构建小组件界面。
// 5. 小组件注册 —— 在 body 中声明所有可用的小组件。
// 6. 模块化设计 —— 每个小组件独立实现和管理。
// 7. 扩展架构 —— 作为独立扩展运行，与主应用分离。
// 8. 声明式语法 —— 使用 SwiftUI 的声明式方式定义小组件集合。
// 9. 系统集成 —— 与 iOS 小组件系统深度集成。
// 10. 统一管理 —— 提供所有小组件的统一入口和管理。
//
// 技术点详解：
// - @main：标记此结构体为小组件扩展的程序入口点，系统从这里开始加载小组件
// - WidgetBundle：允许一个扩展提供多个不同类型的小组件，用户可以分别添加
// - WidgetKit：iOS 14+ 的系统小组件框架，提供完整的小组件生命周期管理
// - SwiftUI：现代化的 UI 框架，简化小组件界面的开发和维护
// - 小组件集合：在 body 中列出所有可用的小组件，系统会自动识别和管理
// - 独立架构：每个小组件都是独立的 Widget 实现，有自己的配置和视图
// - 系统级集成：小组件可以出现在主屏幕、今日视图、锁屏等系统界面
// - 资源管理：扩展有独立的内存和 CPU 限制，需要高效的资源使用
// - 用户体验：提供快速访问应用核心功能的便捷方式
// - 数据更新：支持定时更新和智能刷新机制

// 引入 SwiftUI 框架，用于构建小组件界面
import SwiftUI
// 引入 WidgetKit 框架，提供小组件开发能力
import WidgetKit

// 小组件扩展包的主入口点，管理所有小组件类型
@main
struct IceCubesAppWidgetsExtensionBundle: WidgetBundle {
  // 小组件包的主体，声明所有可用的小组件
  var body: some Widget {
    // 最新帖子小组件 - 显示时间线中的最新帖子
    LatestPostsWidget()
    // 标签帖子小组件 - 显示特定标签的帖子
    HashtagPostsWidget()
    // 列表帖子小组件 - 显示特定列表中的帖子
    ListsPostWidget()
    // 提及通知小组件 - 显示用户被提及的通知
    MentionsWidget()
    // 账户信息小组件 - 显示用户账户的基本信息
    AccountWidget()
  }
}
